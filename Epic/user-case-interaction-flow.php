	<h1><strong>Use Case / Interaction Flow</h1>
	<h2>Use Case:</h2>
	<p><strong>Title:</strong> Finding videos of himself in the movie Suicide Squad</p>
	<p><strong>Description:</strong> Jared wants to use YouTube to find and watch videos of himself playing Joker in the hit movie Suicide Squad</p>
	<p><strong>Name of user and their role:</strong> Jared Leto, casual user of YouTube and lover of his incredible portrayal of Joker in the Suicide Squad</p>
	<p><strong>Usage preconditions:</strong> Jared is on the homepage of YouTube and logged in</p>
	<p><strong>Usage postconditions:</strong>Jared finds a clip of himself playing Joker in Suicide Squad</p>
	<h2>Interaction Flow:</h2>
	<ul>
		<li>Jared enters YouTube.com into his web browser</li>
		<li>Server returns YouTube home page</li>
		<li>Jared clicks the search bar at the top of the page, types, "Jared Leto Suicide Squad clips," and hits enter</li>
		<li>Server returns page containing a list of suicide squad clips with Jared Leto, matching his search</li>
		<li>Jared clicks one of the videos</li>
		<li>The server returns the page with a video player displaying the video Jared clicked on. Jared clicks like </li>
		<li>The server registers that Jared has liked the video. He continues watching happily.</li>
	</ul>
